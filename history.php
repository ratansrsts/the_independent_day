            <h2>A Long Way to Freedom </h2>
            <p> The people of Bangladesh discovered their identity through the Language Movement in 1952. The struggle to establish their identity and national spirit began soon after 1947, when the British left India dividing it into two countries: India and Pakistan. Bangladesh, then East Pakistan, was part of Pakistan, which was put together by combining two geographically, culturally, and linguistically separate groups of people. The people of Bangladesh soon realized that being a part of Pakistan, which was created on the two nation theory, there was little scope for the distance culture of Bangladeshis to flourish. The Bangla language was the most important vehicle of the cultural expression for the people of this land.<br><br> The refusal of the central government in West Pakistan to grant official status to the Bangla became the focal point of struggle.<br><br>

The contradiction of the two Pakistans , the racial oppression and the exploitation of the West over the East was gradually unveiled. The struggle for the consciousness of identity and cultural freedom which began with the advent of the student movements of the 60's gained momentum in the mass movement of non-cooperation in 1969.<br><br> Though it brought about the fall of a mighty military ruler like Ayub Khan, the ultimate goal of self-rule was not achieved.<br> 




 After this, came the election of 1970 with absolute victory of Sheikh Mujibur Rahman.<br><br>

In the elections of December 7, 1970 the Awami League won 160 out of 162 seats in East Pakistan, all but two, and would have had a clear majority in the new assembly had it been convened. Sheikh Mujibur Rahman became the majority party leader of the Pakistan National Assembly .<br><br>



                       



The military rulers of Pakistan refused to allow the Awami League to form a government. A heinous conspiracy was plotted by the then Pakistani military dictator Yahya Khan along with Zulfikar Ali bhutto. Even though a conspiracy was being planned, General Yahya Khan was careful not to let this be known. On the 13th of February he announced that on the 3rd of March there would be a session of National A ssembly in Dhaka. Everyone began to eagerly await that day. Major General Ziaur Rahman on behalf of Bangabandhu Sheikh Mujibur Rahman, announced the declaration of independence from Kalurghat Radio Station.<br><br> A full scale movement of non-cooperation with the military government began on the 26th of March, 1971 which is celebrated as the Independence Day every year. Thus Bangladesh plunged into a gory war seeking its own birth.

The Pakistan Army began their genocide by attacking the innocent Bangaladeshis of Dhaka city.  Sheikh Mujib was imprisoned in west Pakistan. A Bangladesh Government in exile was established. The Bangalees started a smart and courageous guerrilla warfare. At one point, India also got involved in the war. <br><br>The actual military campaign of India took place in December and lasted only ten days.<br> The Indian Army launched a massive offensive against the Pakistani forces to support the Bangladesh movement. On December 16, 1971, the Pakistan army surrendered Bangladesh has got victory.. </p>
  
               
